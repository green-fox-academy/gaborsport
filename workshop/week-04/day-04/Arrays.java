public class Arrays {
    public static void main(String[] args) {
        int[] array = {1, 2, 3, 6};

        // Írasd ki a tömb második elemét
        System.out.println("A tömb második eleme: " + array[1]);

        // Írasd ki a tömb minden elemét
        for (int i = 0; i < array.length; i++) {
            System.out.println(array[i] + " ");
        }
    }
}
